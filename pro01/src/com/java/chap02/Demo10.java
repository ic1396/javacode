package com.java.chap02;

public class Demo10 {
	public static void main(String[] args) {
		int a=2;
		int b=3;
		System.out.println(a+">"+b+": "+(a>b));
		System.out.println(a+"<"+b+": "+(a<b));
		System.out.println(a+">="+b+": "+(a>=b));
		System.out.println(a+"<="+b+": "+(a<=b));
		System.out.println(a+"=="+b+": "+(a==b));
		System.out.println(a+"!="+b+": "+(a!=b));
	}

}
